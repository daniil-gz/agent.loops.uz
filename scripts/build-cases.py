ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
CASES = json.load(open(os.path.join(ROOT, "cases/cases.json"), encoding="utf-8"))

def plain(s):
    return html.unescape(re.sub("<[^>]+>", "", s)).replace("\xa0", " ").strip()

# ---------- cases-data.js (cards) ----------
