#!/usr/bin/env python3
"""
Loops — case generator. SINGLE SOURCE OF TRUTH = cases/cases.json (+ cases/_bodies/<id>.html).
Run:  python3 scripts/build-cases.py
Generates:
  - cases-data.js              (card data + renderer; powers homepage preview, /cases.html, "Другие кейсы")
  - cases/case-<id>/index.html (one detail page per case)
Add/edit a case = edit cases/cases.json + cases/_bodies/<id>.html, then run this script.
Everything (homepage, cases list, detail page, cross-links) updates from the one registry.
"""
